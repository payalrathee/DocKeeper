CREATE DATABASE dockeeper;
USE dockeeper;

CREATE TABLE user(id INT PRIMARY KEY AUTO_INCREMENT, username VARCHAR(100) NOT NULL UNIQUE, fname VARCHAR(100), lname VARCHAR(100), email VARCHAR(500) UNIQUE, phone INT(12) UNIQUE, dp VARCHAR(1000) default 'https://t4.ftcdn.net/jpg/05/89/93/27/360_F_589932782_vQAEAZhHnq1QCGu5ikwrYaQD0Mmurm0N.jpg');

CREATE TABLE doc(id INT PRIMARY KEY AUTO_INCREMENT, name VARCHAR(100), url VARCHAR(500) NOT NULL UNIQUE, note VARCHAR(1000), user_id INT, FOREIGN KEY(user_id) REFERENCES user(id));